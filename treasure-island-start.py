print("You are at a crossroads. To the left is a dark forest, to the right is a beautiful beach.")

direction = input("Which way do you want to go? Left or Right? ").lower()

if direction == "left":
  action = input('what do you want to do? "swim" or "wait"?').lower()
  if action == "wait":
    door_color = input("Which door do you want to open? Red, Blue or Yellow?").lower()
    if door_color == "yellow":
      print("You win")
    elif door_color == "red":
      print("You are burned by fire. Game over.")
    elif door_color == "blue":
      print("You are eaten by beasts. Game over.")
    else:
      print("Game over.")
  else:
    print("You are attacked by a trout. Game over.")
else:
  print("You fall into a hole. Game over.")
